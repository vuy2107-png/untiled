public class VariableDataType {
    public static void main(String[] args) {
        int i = 10;
        float f = 10.5f;
        double d = 10.5;
        boolean b = true;
        char c = 'a';
        String s = "Hello";
        System.out.println(i);
        System.out.println(f);
        System.out.println(d);
        System.out.println(b);
        System.out.println(c);
        System.out.println(s);
    }
}
